package tde.poo;

public class exercicioVinte {
    public static void main(String[] args) {
        int valor = 101;

        if (valor < 15 || valor > 100){
            System.out.println("O valor é menor do que 15 ou maior do que 100 ");
        }
        else {
            System.out.println("O valor é está entre 15 e 100");
        }

        System.out.println("Valor verificado: " + valor);
    }
}
